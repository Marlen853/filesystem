day_number = int(input("Введите номер дня недели (1-7): "))

days_of_week = [
    "Понедельник",
    "Вторник",
    "Среда",
    "Четверг",
    "Пятница",
    "Суббота",
    "Воскресенье",
]

if 1 <= day_number <= 7:
    print(days_of_week[day_number - 1])
else:
    print("Некорректный номер дня недели. Введите число от 1 до 7.")
